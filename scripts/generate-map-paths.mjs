/**
 * Generate SVG path data from Natural Earth 110m land polygons.
 *
 * Uses the same Mercator projection as VisitorMap.tsx so country outlines
 * align perfectly with visitor dots.  Output is a static TS file with zero
 * runtime dependencies.
 *
 * This is a ONE-TIME script — the output (src/data/worldMapPaths.ts) is
 * committed to the repo. Only re-run if you need to change the projection
 * or simplification tolerance.
 *
 * Usage:
 *   npm install --no-save topojson-client world-atlas
 *   node scripts/generate-map-paths.mjs
 */

import { readFileSync, writeFileSync } from 'fs'
import { resolve, dirname } from 'path'
import { fileURLToPath } from 'url'

// topojson-client & world-atlas are devDeps
import { feature } from 'topojson-client'

const __dirname = dirname(fileURLToPath(import.meta.url))

// ---------- projection (matches VisitorMap.tsx) ----------
function project(lat, lng) {
  const x = ((lng + 180) / 360) * 960
  const latRad = (lat * Math.PI) / 180
  const mercN = Math.log(Math.tan(Math.PI / 4 + latRad / 2))
  const y = 240 - (mercN * 960) / (2 * Math.PI)
  return [round(x), round(Math.max(0, Math.min(480, y)))]
}

function round(n) {
  return Math.round(n * 10) / 10 // one decimal place keeps size small
}

// ---------- SVG path helpers ----------
function ringToPath(ring) {
  return ring
    .map(([lng, lat], i) => {
      const [x, y] = project(lat, lng)
      return i === 0 ? `M${x},${y}` : `L${x},${y}`
    })
    .join('')
    + 'Z'
}

function geometryToPath(geometry) {
  if (geometry.type === 'Polygon') {
    return geometry.coordinates.map(ringToPath).join('')
  }
  if (geometry.type === 'MultiPolygon') {
    return geometry.coordinates
      .flatMap((polygon) => polygon.map(ringToPath))
      .join('')
  }
  return ''
}

// ---------- simplify: drop points that barely move the pen ----------
function simplifyRing(ring, tolerance) {
  if (ring.length <= 4) return ring
  const out = [ring[0]]
  for (let i = 1; i < ring.length - 1; i++) {
    const [x0, y0] = project(ring[i - 1][1], ring[i - 1][0])
    const [x1, y1] = project(ring[i][1], ring[i][0])
    if (Math.abs(x1 - x0) > tolerance || Math.abs(y1 - y0) > tolerance) {
      out.push(ring[i])
    }
  }
  out.push(ring[ring.length - 1]) // close
  return out
}

function simplifyGeometry(geom, tolerance) {
  if (geom.type === 'Polygon') {
    return { ...geom, coordinates: geom.coordinates.map((r) => simplifyRing(r, tolerance)) }
  }
  if (geom.type === 'MultiPolygon') {
    return {
      ...geom,
      coordinates: geom.coordinates.map((p) => p.map((r) => simplifyRing(r, tolerance))),
    }
  }
  return geom
}

// ---------- main ----------
const topoPath = resolve(__dirname, '../node_modules/world-atlas/countries-110m.json')
const topo = JSON.parse(readFileSync(topoPath, 'utf8'))

// Use the "land" object for a single combined outline (smaller output)
// Falls back to countries if land isn't available
const objectKey = topo.objects.land ? 'land' : 'countries'
const geojson = feature(topo, topo.objects[objectKey])

const TOLERANCE = 2 // SVG-pixel threshold for point dropping

const allPaths = geojson.features
  ? geojson.features.map((f) => geometryToPath(simplifyGeometry(f.geometry, TOLERANCE))).filter(Boolean)
  : [geometryToPath(simplifyGeometry(geojson.geometry, TOLERANCE))].filter(Boolean)

// Combine into a single path string for minimal DOM nodes
const combined = allPaths.join('')

const output = `// Auto-generated by scripts/generate-map-paths.mjs — do not edit
// Source: Natural Earth 110m via world-atlas
// Projection: Mercator matching VisitorMap (960×480 viewBox)

export const worldMapPath: string =
  ${JSON.stringify(combined)}
`

const outPath = resolve(__dirname, '../src/data/worldMapPaths.ts')
writeFileSync(outPath, output)
console.log(`✓ Wrote combined land path to src/data/worldMapPaths.ts`)
console.log(`  File size: ${(Buffer.byteLength(output) / 1024).toFixed(1)} KB`)
